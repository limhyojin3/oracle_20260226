CREATE OR REPLACE FUNCTION GENDER_FUNC(I_VALUE IN CHAR)
RETURN VARCHAR2
IS
    O_VALUE VARCHAR2(100);
BEGIN
    IF LENGTH(I_VALUE) != 13 THEN
        O_VALUE := '제대로 값을 입력하세요';
    END IF;
    IF SUBSTR(I_VALUE, 7, 1) IN ('1', '3') THEN
        O_VALUE := '남자';
    ELSIF SUBSTR(I_VALUE, 7, 1) IN ('2', '4') THEN
        O_VALUE := '여자';
    ELSE 
        O_VALUE := '해당없음';
    END IF;
    RETURN O_VALUE;
END;
/
--
