-- 날짜별 매출

-- 날짜, 해당 날짜의 매출,
-- 매출이 20000 이상일 경우 '상', 10000 이상일 경우 '중'
-- 그외는 '하'로 표기

SELECT * 
FROM ORDERS;
--

SELECT 
    TO_CHAR(ORDERDATE, 'YY/MM/DD'),
    SUM(SALEPRICE),
    CASE
        WHEN SUM(SALEPRICE) >= 20000 THEN '상'  --WHEN(조건절)THEN(행에 표시)
        WHEN SUM(SALEPRICE) >= 10000 THEN '중'
        ELSE '하'
    END 상중하
FROM ORDERS
GROUP BY TO_CHAR(ORDERDATE, 'YY/MM/DD');
