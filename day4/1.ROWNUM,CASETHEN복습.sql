SELECT 날짜, SUM_SALEPRICE
FROM (
    SELECT 
        TO_CHAR(ORDERDATE, 'YYYY/MM/DD') AS 날짜,
        SUM(SALEPRICE) SUM_SALEPRICE,
        1 AS ORDERKEY
    FROM ORDERS
    GROUP BY ORDERDATE
    
    UNION
    SELECT 
        '매출총액',
        SUM(SALEPRICE),
        2 AS ORDERKEY
    FROM ORDERS
    ORDER BY ORDERKEY, SUM_SALEPRICE DESC
);

